#type string (Python uses Str as its default Data Type)
y = "John"
print(type(y))

#type integer
x = 8
print(type(x))

#if we put double or single quotes then it will be a string
z = "30"
print(type(z))

#type float
a=9.66
print(type(a))

#type single character will also be taken as string
b='w'
print(type(b))

#type complex j represents complex nos.
p=4j
q=45.j
r=3.14j
s=2+32j
print(type(p))
print(type(q))
print(type(r))
print(type(s))