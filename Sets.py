# Set items are unordered, unchangeable, and do not allow duplicate values.
''' 
    Note: Set items are unchangeable, but you can remove items and 
    add new items.
'''

# Creating a set
Set1 = { 1, 2, 1, 3, 4, 5 }
print(Set1)

# Sets can be made from lists
my_set = set([1, 2, 3, 2])
print(my_set)
