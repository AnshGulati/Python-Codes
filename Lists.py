# List is a collection which is ordered , changeable and allows duplicate members.

''' A list in Python is used to store the sequence of various types of data. Python lists are 
    mutable type it mean we can modify its element after it created.
    Unlike arrays in other programming languages, lists can store heterogeneous items
    Can be written as a list of comma-separated values (items) between square brackets.
'''

Height=["Rahul",1.75,"Mehul",1.63]
print(Height)

# Nesting Lists
Person_Height=[["Rahul",1.75],["Mehul",1.63]]
print(Person_Height)

# Duplicates are allowed
thislist = ["apple", "banana", "cherry", "apple", "cherry"]
print(thislist)
